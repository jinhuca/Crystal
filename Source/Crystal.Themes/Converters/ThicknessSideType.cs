namespace Crystal.Themes.Converters;

public enum ThicknessSideType
{
  /// <summary>
  /// Use all sides.
  /// </summary>
  None,
  /// <summary>
  /// Ignore the left side.
  /// </summary>
  Left,
  /// <summary>
  /// Ignore the top side.
  /// </summary>
  Top,
  /// <summary>
  /// Ignore the right side.
  /// </summary>
  Right,
  /// <summary>
  /// Ignore the bottom side.
  /// </summary>
  Bottom
}